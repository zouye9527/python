#选择乘车时段
isSpecialtime = input("请问你是否在特殊时段乘车（是\否）：")

#输入已知条件
s = eval(input("请输入你乘车的路程(km)："))
t = eval(input("请输入你乘车的时间(分钟)："))


#分段求解计算：请将下划线去掉补充好相应代码
if isSpecialtime != "是":   
    f = 1.4 * s + t * 0.4 #普通时段
else:
    f = 1.7 * s + t* 0.4 #特殊时段

#当里程费和时长费合计不足基础费时，需用基础费计费9元
if f < 9:
   f = 9
        
#输出结果
print("预估车费为", format(f,".2f"), "元。")
