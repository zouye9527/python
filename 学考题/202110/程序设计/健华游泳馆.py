"""
健华游泳馆的泳池中包含a吨水，在全部更换新水时，需要将原有的旧水全部排空，假设其排水系统的设计能力为每分钟b吨，则旧水全部排完需要多少分钟
输入输出说明：
1、输入有2行，每行有1个数；第1行中的数是a的值，第2行中的数是b的值
2、输出有1行，为程序计算的结果（保留两位小数）；如果输入中的b≤0，则输出“排水速度必须大于0”
输入输出样例：
样例1：
输入：
1500
50
输出:
30.00
样例2：
输入：
1500
0
输出：
排水速度必须大于0
"""
a=float(input())
b=float(input())
if b<=0:
    print("排水速度必须大于0")
else:
    v=a/b
    print("%.2f"%v)
