"""
使用type()检查变量的类型


"""

a = 100
b = 12.345
c = 1 + 5j
d = 'hello, world'
e = True
f=["红楼梦","水浒传","三国演义","西游记"]
print(type(a))    # <class 'int'>
print(type(b))    # <class 'float'>
print(type(c))    # <class 'complex'>
print(type(d))    # <class 'str'>
print(type(e))    # <class 'bool'>
print(type(f))    # <class 'list'>
print(len(f))
print(min(f))
print(max(f))
pop(f[1])
print(f)
